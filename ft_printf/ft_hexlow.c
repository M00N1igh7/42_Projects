#include "ft_printf.h"

int	ft_hexlow(unsigned int n, int i)
{
	if (n)
	{
		i++;
		if ((n / 16))
			i = ft_hexlow((n / 16), i);
		write(1, &"0123456789abcdef"[(n % 16)], 1);
		return (i);
	}
	else
	{
		i++;
		write(1, "0", 1);
	}
	return (i);
}
