#include "ft_printf.h"

int	ft_hexup(unsigned int n, int i)
{
	if (n)
	{
		i++;
		if ((n / 16))
			i = ft_hexup((n / 16), i);
		write(1, &"0123456789ABCDEF"[(n % 16)], 1);
		return (i);
	}
	else
	{
		i++;
		write(1, "0", 1);
	}
	return (i);
}
