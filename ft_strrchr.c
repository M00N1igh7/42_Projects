char	*ft_strrchr(const char *s, int c)
{
	int	i[2];
	int	save;

	i[0] = 0;
	i[1] = 0;
	save = 0;
	while (s[i[0]] != '\0')
	{
		if (s[i[0]] == (char)c)
		{
			save = i[0];
			i[1]++;
		}
		i[0]++;
		if (s[i[0]] == (char)c)
		{
			save = i[0];
			i[1]++;
		}
	}
	if (i[1] != 0)
		return ((char *)(s + save));
	if (s[0] == (char)c)
		return ((char *)s);
	return (0);
}
